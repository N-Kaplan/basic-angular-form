export class Friend {
  firstName:string;
  lastName:string;
  email:string;
  phoneNr:string;
  favLanguage:string;

  constructor(firstName: string, lastName: string, email: string, phoneNr: string, favLanguage: string) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.phoneNr = phoneNr;
    this.favLanguage = favLanguage;
  }

}
